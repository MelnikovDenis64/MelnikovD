# Раздел: Объявление переменных
my_city = "Saratov"
print(my_city)

# Раздел: Операции сравнения
a, b = 15, 10
result = a > b
print(result)

# Раздел: Преобразование типов данных
price = "150"
int_price = int(price)
result_price = int_price + 50
print(result_price)

# Раздел: Арифметические операции
x, y = 20, 6
result = x // y
print(result)

# Раздел: Типы данных
pi = 3.14
name = "Denis"
boolean = False
print(type(pi), type(name), type(boolean))

# Раздел: Именование переменных
number_of_students = 25
print(number_of_students)

# Раздел: Переопределение переменной
