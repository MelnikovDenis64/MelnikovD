# Задача 1 (Базовые методы строк)
# У вас есть строка: text = " привет, мир! "
# Напишите код, который:
# Удалит все пробелы в начале и конце строки.
# Сделает все буквы заглавными.
# Заменит слово "мир" на "Python".
# Выведите результат на экран.

text = " привет, мир! "
strip_text = text.strip()
new_text = strip_text.replace("мир", "Python")
upp_text = new_text.upper()
print(upp_text)


