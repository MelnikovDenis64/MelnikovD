# Задача 3 (Срезы)
# У вас есть строка: alphabet = "abcdefghijklmnopqrstuvwxyz"
# Напишите код, который с помощью срезов выведет на экран следующие подстроки:
# Первые 5 символов.
# Последние 3 символа.
# Символы с 10-го по 15-й (включительно).
# Все символы с четными индексами (0, 2, 4...).

alphabet = "abcdefghijklmnopqrstuvwxyz"
first_5_characters = alphabet[0:5]
print(first_5_characters)
last_3_characters = alphabet[-3:]
print(last_3_characters)
characters_10_15 = alphabet[9:15]
print(characters_10_15)
even_characters = alphabet[::2]
print(even_characters)


