# Задача 6 (Получение длины строки)
# У вас есть строка: sentence = "Сколько символов в этой строке?"
# Напишите код, который:
# Вычислит длину этой строки.
# Проверит, является ли длина строки чётным числом.
# Выведет на экран сообщение: "Длина строки: [число]. Чётная: [True/False]"

sentence = "Сколько символов в этой строке?"
