# **Задача 27**
# У вас есть два списка:
# list1 = [1, 2, 3, 4, 5]
# list2 = [10, 20, 30]
# Нужно:
# 1. Взять первые 3 элемента из `list1`
# 2. Добавить к ним все элементы из `list2`
# 3. Затем добавить оставшиеся элементы из `list1` (те, которые не вошли в первые 3)
# 4. Вывести итоговый список
# Получиться должно: `[1, 2, 3, 10, 20, 30, 4, 5]`

list1 = [1, 2, 3, 4, 5]
list2 = [10, 20, 30]
list_result = list1[:3] + list2 + list1[3:]
print(list_result)
